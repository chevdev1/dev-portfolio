// –¢–µ–∫—É—â–∏–π —è–∑—ã–∫ (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é —Ä—É—Å—Å–∫–∏–π)
let currentLanguage = localStorage.getItem('language') || 'ru';

/**
 * –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è —Å–∏—Å—Ç–µ–º—ã –ø–µ—Ä–µ–≤–æ–¥–æ–≤
 */
function initializeTranslations() {
  console.log('üåç –°–∏—Å—Ç–µ–º–∞ –ø–µ—Ä–µ–≤–æ–¥–æ–≤ –∑–∞–≥—Ä—É–∂–µ–Ω–∞');
  
  // –ù–∞—Å—Ç—Ä–∞–∏–≤–∞–µ–º –ø–µ—Ä–µ–∫–ª—é—á–∞—Ç–µ–ª–∏ —è–∑—ã–∫–æ–≤
  setupLanguageSwitchers();
  
  // –ü—Ä–∏–º–µ–Ω—è–µ–º —Å–æ—Ö—Ä–∞–Ω–µ–Ω–Ω—ã–π —è–∑—ã–∫
  changeLanguage(currentLanguage, false);
  
  console.log(`‚úÖ –ê–∫—Ç–∏–≤–Ω—ã–π —è–∑—ã–∫: ${currentLanguage === 'ru' ? '–†—É—Å—Å–∫–∏–π' : '–£–∫—Ä–∞—ó–Ω—Å—å–∫–∞'}`);
}

/**
 * –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –ø–µ—Ä–µ–∫–ª—é—á–∞—Ç–µ–ª–µ–π —è–∑—ã–∫–æ–≤
 */
function setupLanguageSwitchers() {
  const ruBtn = document.getElementById('lang-ru');
  const ukBtn = document.getElementById('lang-uk');
  
  if (!ruBtn || !ukBtn) {
    console.warn('‚ö†Ô∏è –ö–Ω–æ–ø–∫–∏ –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è —è–∑—ã–∫–æ–≤ –Ω–µ –Ω–∞–π–¥–µ–Ω—ã');
    return;
  }
  
  // –û–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ –∫–ª–∏–∫–æ–≤
  ruBtn.addEventListener('click', () => changeLanguage('ru'));
  ukBtn.addEventListener('click', () => changeLanguage('uk'));
  
  console.log('‚úÖ –ü–µ—Ä–µ–∫–ª—é—á–∞—Ç–µ–ª–∏ —è–∑—ã–∫–æ–≤ –Ω–∞—Å—Ç—Ä–æ–µ–Ω—ã');
}

/**
 * –°–º–µ–Ω–∞ —è–∑—ã–∫–∞
 * @param {string} lang - –ö–æ–¥ —è–∑—ã–∫–∞ (ru/uk)
 * @param {boolean} animate - –í–∫–ª—é—á–∏—Ç—å –∞–Ω–∏–º–∞—Ü–∏—é
 */
function changeLanguage(lang, animate = true) {
  if (!translations[lang]) {
    console.error(`‚ùå –ü–µ—Ä–µ–≤–æ–¥—ã –¥–ª—è —è–∑—ã–∫–∞ "${lang}" –Ω–µ –Ω–∞–π–¥–µ–Ω—ã`);
    return;
  }
  
  currentLanguage = lang;
  
  // –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ localStorage
  localStorage.setItem('language', lang);
  
  // –û–±–Ω–æ–≤–ª—è–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ –∫–Ω–æ–ø–æ–∫
  updateLanguageButtons();
  
  // –ü—Ä–∏–º–µ–Ω—è–µ–º –ø–µ—Ä–µ–≤–æ–¥—ã
  applyTranslations(lang, animate);
  
  console.log(`üîÑ –Ø–∑—ã–∫ –∏–∑–º–µ–Ω–µ–Ω –Ω–∞: ${lang.toUpperCase()}`);
}

/**
 * –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å–æ—Å—Ç–æ—è–Ω–∏—è –∫–Ω–æ–ø–æ–∫ —è–∑—ã–∫–æ–≤
 */
function updateLanguageButtons() {
  const ruBtn = document.getElementById('lang-ru');
  const ukBtn = document.getElementById('lang-uk');
  
  if (!ruBtn || !ukBtn) return;
  
  // –£–±–∏—Ä–∞–µ–º active —Å–æ –≤—Å–µ—Ö –∫–Ω–æ–ø–æ–∫
  ruBtn.classList.remove('active');
  ukBtn.classList.remove('active');
  
  // –î–æ–±–∞–≤–ª—è–µ–º active –∫ —Ç–µ–∫—É—â–µ–π
  if (currentLanguage === 'ru') {
    ruBtn.classList.add('active');
  } else {
    ukBtn.classList.add('active');
  }
}

/**
 * –ü—Ä–∏–º–µ–Ω–µ–Ω–∏–µ –ø–µ—Ä–µ–≤–æ–¥–æ–≤ –∫–æ –≤—Å–µ–º —ç–ª–µ–º–µ–Ω—Ç–∞–º
 * @param {string} lang - –ö–æ–¥ —è–∑—ã–∫–∞
 * @param {boolean} animate - –í–∫–ª—é—á–∏—Ç—å –∞–Ω–∏–º–∞—Ü–∏—é
 */
function applyTranslations(lang, animate = true) {
  const elements = document.querySelectorAll('[data-translate]');
  
  elements.forEach(element => {
    const key = element.getAttribute('data-translate');
    const translation = translations[lang][key];
    
    if (translation) {
      // –ê–Ω–∏–º–∞—Ü–∏—è —Å–º–µ–Ω—ã —Ç–µ–∫—Å—Ç–∞
      if (animate) {
        element.style.opacity = '0.7';
        element.classList.add('language-transition');
        
        setTimeout(() => {
          element.textContent = translation;
          element.style.opacity = '1';
          
          setTimeout(() => {
            element.classList.remove('language-transition');
          }, 300);
        }, 150);
        
      } else {
        element.textContent = translation;
      }
    } else {
      console.warn(`‚ö†Ô∏è –ü–µ—Ä–µ–≤–æ–¥ –Ω–µ –Ω–∞–π–¥–µ–Ω –¥–ª—è –∫–ª—é—á–∞: ${key}`);
    }
  });
  
  // –û–±–Ω–æ–≤–ª—è–µ–º title —Å—Ç—Ä–∞–Ω–∏—Ü—ã
  updatePageTitle(lang);
  
  // –û–±–Ω–æ–≤–ª—è–µ–º placeholder'—ã –≤ —Ñ–æ—Ä–º–µ
  updateFormPlaceholders(lang);
}

/**
 * –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ title —Å—Ç—Ä–∞–Ω–∏—Ü—ã
 * @param {string} lang - –ö–æ–¥ —è–∑—ã–∫–∞
 */
function updatePageTitle(lang) {
  const titles = {
    'ru': '–ö—Ä–µ–∞—Ç–∏–≤ ‚Äî –†–∞–∑—Ä–∞–±–æ—Ç–∫–∞ –ø–æ–¥ –∫–ª—é—á',
    'uk': '–ö—Ä–µ–∞—Ç–∏–≤ ‚Äî –†–æ–∑—Ä–æ–±–∫–∞ –ø—ñ–¥ –∫–ª—é—á'
  };
  
  document.title = titles[lang] || titles.ru;
}

/**
 * –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ placeholder'–æ–≤ –≤ —Ñ–æ—Ä–º–µ
 * @param {string} lang - –ö–æ–¥ —è–∑—ã–∫–∞
 */
function updateFormPlaceholders(lang) {
  const formElements = [
    { selector: 'input[name="name"]', key: 'form-name' },
    { selector: 'input[name="email"]', key: 'form-email' },
    { selector: 'input[name="contact"]', key: 'form-contact' },
    { selector: 'textarea[name="task"]', key: 'form-task' }
  ];
  
  formElements.forEach(({ selector, key }) => {
    const element = document.querySelector(selector);
    if (element && translations[lang][key]) {
      element.placeholder = translations[lang][key];
    }
  });
}

/**
 * –ü–æ–ª—É—á–µ–Ω–∏–µ —Ç–µ–∫—É—â–µ–≥–æ —è–∑—ã–∫–∞
 * @returns {string} –ö–æ–¥ —Ç–µ–∫—É—â–µ–≥–æ —è–∑—ã–∫–∞
 */
function getCurrentLanguage() {
  return currentLanguage;
}

/**
 * –ü–æ–ª—É—á–µ–Ω–∏–µ –ø–µ—Ä–µ–≤–µ–¥–µ–Ω–Ω–æ–≥–æ —Ç–µ–∫—Å—Ç–∞
 * @param {string} key - –ö–ª—é—á –ø–µ—Ä–µ–≤–æ–¥–∞
 * @param {string} lang - –ö–æ–¥ —è–∑—ã–∫–∞ (–Ω–µ–æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–π)
 * @returns {string} –ü–µ—Ä–µ–≤–µ–¥–µ–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç
 */
function getTranslation(key, lang = currentLanguage) {
  return translations[lang]?.[key] || translations.ru[key] || key;
}

